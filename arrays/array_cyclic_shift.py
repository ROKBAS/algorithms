# Циклический сдвиг
# Влево и вправо
# 0 1 2 3 4
# 1 2 3 4 0
# Метод грузчика на тесном складе
# Задать tmp
# Слева на право
A = [0,1,2,3,4]
N = 5
tmp = A[0]
for k in range(N-1):
    A[k] = A[k+1]
A[N-1] = tmp
print(A)
# Справа налево
tmp = A[N-1]
for k in range(N-2,-1,-1):
    A[k+1] = A[k]
A[0] = tmp
print(A)